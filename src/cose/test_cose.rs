fn test_rfc6979_test_vector_cose_1() {
    test::setup();
    let payload = b"This is the content.";

    // Building the COSE signature manually.
    // Tagged (98) array of length 4.
    let mut cose_signature = vec![0xD8, 0x62, 0x84];
    // add cert chain as protected header
    cose_signature.extend_from_slice(&CERT_CHAIN);
    cose_signature.push(0xA0); // Empty map (unprotected header).
    // TODO #22: The content should be nil.
    cose_signature.push(0x40); // Empty bytes (content).
    // TODO #15: Handle multiple signatures
    cose_signature.push(0x81); // Array with 1 element.

    // Now add a COSE siganture element.
    cose_signature.push(0x83); // Array with 3 element.
    cose_signature.extend_from_slice(&PROTECTED_SIGNATURE_HEADER);
    // TODO #22: The content should be nil.
    cose_signature.push(0x40); // Empty bytes (unprotected header).
    cose_signature.push(0x58); // 64 bytes (the signature).
    cose_signature.push(0x40);
    cose_signature.extend_from_slice(&SIGNATURE_BYTES); // signature bytes

    // Verify the signature.
    assert!(cose::verify_signature(payload, cose_signature).is_ok());
}
